/**
 * Change the language of the page
 * @param {string} lang - The language code to switch to (en, de, bs)
 */
function setLanguage(lang) {
  // Store language
  localStorage.setItem('selectedLanguage', lang);

  // Set fallback just in case
  const dict = translations[lang] || translations['en'];

  // Update simple text elements
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });

  // Update input/textarea placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key]) {
      el.placeholder = dict[key];
    }
  });

  // Update meta tags for description
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && dict['meta_desc']) {
    metaDesc.setAttribute("content", dict['meta_desc']);
  }

  const ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc && dict['meta_desc']) {
    ogDesc.setAttribute("content", dict['meta_desc']);
  }

  const twitterDesc = document.querySelector('meta[name="twitter:description"]');
  if (twitterDesc && dict['meta_desc']) {
    twitterDesc.setAttribute("content", dict['meta_desc']);
  }

  // Update html lang attribute
  document.documentElement.lang = lang;

  // Make sure language selector UI reflects the current language
  const selector = document.getElementById('lang-selector');
  if (selector) {
    selector.value = lang;
  }
}

// Initialization on DOMContentLoaded
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('selectedLanguage') || 'en';
  setLanguage(savedLang);
});